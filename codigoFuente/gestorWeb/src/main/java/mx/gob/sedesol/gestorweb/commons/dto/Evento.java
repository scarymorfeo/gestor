package mx.gob.sedesol.gestorweb.commons.dto;

public class Evento {
	private String rutaImagenCurso;
	private String objetivoGeneralCurso;
	private String nombreCurso;
	private String perfil;
	private String requisitos;
	private String modalidadCurso;
	private String valoracionCurso;
	private String fechaInicioCurso;
	private String fechaFinalCurso;
	private String duracionCurso;

	public String getRutaImagenCurso() {
		return rutaImagenCurso;
	}

	public void setRutaImagenCurso(String rutaImagenCurso) {
		this.rutaImagenCurso = rutaImagenCurso;
	}

	public String getNombreCurso() {
		return nombreCurso;
	}

	public void setNombreCurso(String nombreCurso) {
		this.nombreCurso = nombreCurso;
	}

	public String getPerfil() {
		return perfil;
	}

	public void setPerfil(String perfil) {
		this.perfil = perfil;
	}

	public String getRequisitos() {
		return requisitos;
	}

	public void setRequisitos(String requisitos) {
		this.requisitos = requisitos;
	}

	public String getModalidadCurso() {
		return modalidadCurso;
	}

	public void setModalidadCurso(String modalidadCurso) {
		this.modalidadCurso = modalidadCurso;
	}

	public String getValoracionCurso() {
		return valoracionCurso;
	}

	public void setValoracionCurso(String valoracionCurso) {
		this.valoracionCurso = valoracionCurso;
	}

	public String getFechaInicioCurso() {
		return fechaInicioCurso;
	}

	public void setFechaInicioCurso(String fechaInicioCurso) {
		this.fechaInicioCurso = fechaInicioCurso;
	}

	public String getFechaFinalCurso() {
		return fechaFinalCurso;
	}

	public void setFechaFinalCurso(String fechaFinalCurso) {
		this.fechaFinalCurso = fechaFinalCurso;
	}

	public String getDuracionCurso() {
		return duracionCurso;
	}

	public void setDuracionCurso(String duracionCurso) {
		this.duracionCurso = duracionCurso;
	}

	public String getObjetivoGeneralCurso() {
		return objetivoGeneralCurso;
	}

	public void setObjetivoGeneralCurso(String objetivoGeneralCurso) {
		this.objetivoGeneralCurso = objetivoGeneralCurso;
	}

}
