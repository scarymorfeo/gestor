package mx.gob.sedesol.gestorweb.commons.dto;

public class CargaHorariaPrograma {
	
	private String horasTeoria;
	private String minutosTeoria;
	
	private String horasPractica;
	private String minutosPractica;
	
	private String horasEvaluacion;
	private String minutosEvaluacion;
	
	public String getHorasTeoria() {
		return horasTeoria;
	}
	public void setHorasTeoria(String horasTeoria) {
		this.horasTeoria = horasTeoria;
	}
	public String getMinutosTeoria() {
		return minutosTeoria;
	}
	public void setMinutosTeoria(String minutosTeoria) {
		this.minutosTeoria = minutosTeoria;
	}
	public String getHorasPractica() {
		return horasPractica;
	}
	public void setHorasPractica(String horasPractica) {
		this.horasPractica = horasPractica;
	}
	public String getMinutosPractica() {
		return minutosPractica;
	}
	public void setMinutosPractica(String minutosPractica) {
		this.minutosPractica = minutosPractica;
	}
	public String getHorasEvaluacion() {
		return horasEvaluacion;
	}
	public void setHorasEvaluacion(String horasEvaluacion) {
		this.horasEvaluacion = horasEvaluacion;
	}
	public String getMinutosEvaluacion() {
		return minutosEvaluacion;
	}
	public void setMinutosEvaluacion(String minutosEvaluacion) {
		this.minutosEvaluacion = minutosEvaluacion;
	}

	
}
